package permissions

// AssetTemplate Permissions
const (
	// AssetTemplateList - Permission to list all asset templates
	AssetTemplateList = "assettemplates.list"

	// AssetTemplateCreate - Permission to create a new asset template
	AssetTemplateCreate = "assettemplates.create"

	// AssetTemplateRead - Permission to read a specific asset template
	AssetTemplateRead = "assettemplates.read"

	// AssetTemplateUpdate - Permission to update an asset template
	AssetTemplateUpdate = "assettemplates.update"

	// AssetTemplateDelete - Permission to delete an asset template
	AssetTemplateDelete = "assettemplates.delete"

	// AssetTemplateAll - Wildcard permission for all asset template operations
	AssetTemplateAll = "assettemplates.*"
)

package permissions

// Asset Permissions
const (
	// AssetList - Permission to list all assets
	AssetList = "assets.list"

	// AssetCreate - Permission to create a new assets
	AssetCreate = "assets.create"

	// AssetRead - Permission to read a specific assets
	AssetRead = "assets.read"

	// AssetUpdate - Permission to update an assets
	AssetUpdate = "assets.update"

	// AssetDelete - Permission to delete an assets
	AssetDelete = "assets.delete"

	// AssetAll - Wildcard permission for all assets operations
	AssetAll = "assets.*"
)

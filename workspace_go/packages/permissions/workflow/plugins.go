package permissions

// Plugin Permissions
const (
	// PluginList - Permission to list all plugins
	PluginList = "plugins.list"

	// PluginCreate - Permission to create a new plugin
	PluginCreate = "plugins.create"

	// PluginRead - Permission to read a specific plugin
	PluginRead = "plugins.read"

	// PluginUpdate - Permission to update a plugin
	PluginUpdate = "plugins.update"

	// PluginDelete - Permission to delete a plugin
	PluginDelete = "plugins.delete"

	// PluginAll - Wildcard permission for all plugin operations
	PluginAll = "plugins.*"
)

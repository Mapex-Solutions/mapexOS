package permissions

// Group Permissions
const (
	// GroupList - Permission to list all groups
	GroupList = "groups.list"

	// GroupCreate - Permission to create a new groups
	GroupCreate = "groups.create"

	// GroupRead - Permission to read a specific groups
	GroupRead = "groups.read"

	// GroupUpdate - Permission to update a groups
	GroupUpdate = "groups.update"

	// GroupDelete - Permission to delete a groups
	GroupDelete = "groups.delete"

	// GroupAll - Wildcard permission for all groups operations
	GroupAll = "groups.*"
)

package permissions

// Role Permissions
const (
	// RoleList - Permission to list all roles
	RoleList = "roles.list"

	// RoleCreate - Permission to create a new roles
	RoleCreate = "roles.create"

	// RoleRead - Permission to read a specific roles
	RoleRead = "roles.read"

	// RoleUpdate - Permission to update a roles
	RoleUpdate = "roles.update"

	// RoleDelete - Permission to delete a roles
	RoleDelete = "roles.delete"

	// RoleAll - Wildcard permission for all roles operations
	RoleAll = "roles.*"
)
